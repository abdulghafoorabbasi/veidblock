package org.acreo.proposal.launch.commons;

public class PlanningProposal {
	
	String proposalName;
	String proposalType;
	String geoLocation;
	String proposalURL;
	String proposalAddress;
	String proposalDescription;
	
	public String getProposalName() {
		return proposalName;
	}
	public void setProposalName(String proposalName) {
		this.proposalName = proposalName;
	}
	public String getProposalType() {
		return proposalType;
	}
	public void setProposalType(String proposalType) {
		this.proposalType = proposalType;
	}
	public String getGeoLocation() {
		return geoLocation;
	}
	public void setGeoLocation(String geoLocation) {
		this.geoLocation = geoLocation;
	}
	public String getProposalURL() {
		return proposalURL;
	}
	public void setProposalURL(String proposalURL) {
		this.proposalURL = proposalURL;
	}
	public String getProposalAddress() {
		return proposalAddress;
	}
	public void setProposalAddress(String proposalAddress) {
		this.proposalAddress = proposalAddress;
	}
	public String getProposalDescription() {
		return proposalDescription;
	}
	public void setProposalDescription(String proposalDescription) {
		this.proposalDescription = proposalDescription;
	}
}
